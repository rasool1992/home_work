// Create two string variables: one with your first name and one with your last name. Concatenate and print the full name.
void main() {
  String firstName = "Rasool";
  String lastName = "Ali";

  // concatenation using + operator:
  print(firstName + " " + lastName);

  // concatenation using String Interpolation $
  print("$firstName $lastName");
}
