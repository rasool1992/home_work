// Question 8.
// Write a Dart program that declares two numeric variables and performs addition, subtraction, multiplication, division, and modulo on them. Print each result.

void main() {
  num deg1 = 10;
  num deg2 = 20;
  print('the summation of $deg1 + $deg2 = ${deg1 + deg2}');
  print('the substraction of $deg1 - $deg2 = ${deg1 - deg2}');
  print('the multiplication of $deg1 * $deg2 = ${deg1 * deg2}');
  print('the division of $deg1 / $deg2 = ${deg1 / deg2}');
  print('the reminder of $deg1 % $deg2 = ${deg1 % deg2}');
}
